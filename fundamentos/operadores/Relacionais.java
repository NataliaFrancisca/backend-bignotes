public class Relacionais {
    public static void main(String[] args) {
        int valorA = 1;
        int valorB = 3;

        if (valorA > valorB){
            System.out.println("maior");
        }

        if (valorA >= valorB){
            System.out.println("maior ou igual");
        }

        if (valorA < valorB){
            System.out.println("menor");
        }

        if (valorA <= valorB){
            System.out.println("menor ou igual");
        }
    }
}
